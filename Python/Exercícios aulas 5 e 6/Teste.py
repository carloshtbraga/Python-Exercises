print('Contador de números ímpares com while:')
c = 1
while c <=10:
    print(c)
    c+=2
    
print('Contador de números pares com while:')
c = 0
while c <= 10:
    print(c)
    c+=2
    
print('Contador decrescente de números pares com while:')
c = 10
while c >=0:
    print(c)
    c-=2

print('Contador decrescente de números ímpares com while:')
c = 9
while c >=1:
    print(c)
    c-=2
    

